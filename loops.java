

public class loops {
    public static void main(String[] args) {
        // for loop
        for(int i=0; i<5; i++){
            System.out.println("Hello World!");
        }

        //while loop
        int i =0;
        while(i<5){
            System.out.println(i + " Hello World!");
            i++;

        }

        //do while loop
         i=0;
        do{
            System.out.println(i + " Do While Loop");
            i++;
        }while(i < 5);
    }
}
